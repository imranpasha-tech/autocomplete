package com.search.feature;


/**
 *  Acts as an <i>Entity</i> class.
 * @author imran.pasha
 *
 */
public class School {
	private String city;
	private int rank;
	private String state;
	
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public int getRank() {
		return rank;
	}
	public void setRank(int rank) {
		this.rank = rank;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	
	
}
